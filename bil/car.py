class car:
    # Constructor
    def__int__(self, make, color, lager)
    self.make = make
    self.color = color
    self.lager = lager
    
    def getmake(self, make):
        return self.make

    def getcolor(self, color):
        return self.color

    def getlager(self, lager):
        return self.lager

    def setmake(self, make):
        self.make = make
    
    def setcolor(self, color):
        self.color = color

    def setlager(self, lager):
        self.lager = lager
